void OledSsd1306::Display(bool wifiConnected, bool mqttConnected,
                          float airTemperature, float airHumidity,
                          float averageSoilMoisture) {
  this->getDisplay()->clearDisplay();

  this->getDisplay()->setTextColor(SSD1306_WHITE);

  this->getDisplay()->setTextSize(2);
  this->getDisplay()->setCursor(0, 0);
  this->getDisplay()->print(this->getHeader());

  this->getDisplay()->setTextSize(1);

  this->getDisplay()->setCursor(0, 16);
  this->getDisplay()->print("WIFI:");
  this->getDisplay()->setCursor(54, 16);
  this->getDisplay()->print(wifiConnected ? "   Connected" : "Disconnected");

  this->getDisplay()->setCursor(0, 24);
  this->getDisplay()->print("MQTT:");
  this->getDisplay()->setCursor(54, 24);
  this->getDisplay()->print(mqttConnected ? "   Connected" : "Disconnected");

  this->getDisplay()->setCursor(0, 40);
  this->getDisplay()->print("Air Temp:");
  this->getDisplay()->setCursor(90, 40);
  this->getDisplay()->print(this->floatToString_6(airTemperature));

  this->getDisplay()->setCursor(0, 48);
  this->getDisplay()->print("Air Hum:");
  this->getDisplay()->setCursor(90, 48);
  this->getDisplay()->print(this->floatToString_6(airHumidity));

  this->getDisplay()->setCursor(0, 56);
  this->getDisplay()->print("Soil Moist:");
  this->getDisplay()->setCursor(90, 56);
  this->getDisplay()->print(this->floatToString_6(averageSoilMoisture));

  this->getDisplay()->display();
}

void OledSsd1306::Lighting(bool on) {
  this->getDisplay()->ssd1306_command(on ? SSD1306_DISPLAYON
                                         : SSD1306_DISPLAYOFF);
}

